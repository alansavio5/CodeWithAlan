name=input()
print(type(name))   #datatype of variable
print(len(name))    #length of string
print(name.find("A"))   #find the alphabet from the string
print(name.capitalize())    #capitalize the first character
print(name.upper())     #turn into uppercase
print(name.lower())     #turn into lowercase
print(name.isdigit())   #to check if it is a digit or not
print(name.isalpha())   #to check if it is an alphabet
print(name.count("A"))  #to count the number of characters
print(name.replace("a","e"))    #to replace a character with another character
print(name*3)       #to display a string multiple times
print(name.split(" "))